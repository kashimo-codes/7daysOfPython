#  Learning about Loops, Functions, modules and libraries
# Loops (for/while)
# for loops:
# piece of code executed once for each element
# of a sequence (such as string, tuple or list)
languages = ['Python','JavaScript','Go']

for lang in languages:
    print(lang)

#  while loop 
# execute a block of code repeatedly as long as
# condition falls to True.

i = 1
n = 5
#  while loop from i = 1 to 5
while i <= n:
    print(i)
    i +=1
