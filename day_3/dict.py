# Dictionaries
#  stores a collection of key-value pairs.
#  Keys are unique and unchangeable (strings & numbers are common)
# Values can be any type

my_info = {"name": "Tity Junior", "project":"7 Days of Python", "country": "South Africa"}
print(my_info["name"]) # prints out the value associated with the name key
print(my_info["project"]) # prints out the value associated with the project key
print(my_info["country"]) # prints out the value associated with the country key